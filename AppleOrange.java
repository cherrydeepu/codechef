import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner s=new Scanner(System.in);
	    int X=s.nextInt();
	    int A=s.nextInt();
	    int B=s.nextInt();
	    if((A+B)<=X){
	        System.out.println("Yes");
	    }else{
	        System.out.println("No");
	    }
	}
}
