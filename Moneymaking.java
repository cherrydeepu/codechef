import java.util.*;
class Moneymaking{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int X=sc.nextInt();
        int Y=sc.nextInt();
        int c=5000;
        int d=9800;
        X=X*c;
        Y=Y*d;
        int Z=X+Y;
        System.out.println(Z);
    }
}